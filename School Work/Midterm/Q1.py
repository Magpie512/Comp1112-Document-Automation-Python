print ((13%5) - (13//3))

# Beakdown

# 13  %   5 = 3 (remainder of 13 divided by 5)
# Modulo operator (%) gives the remainder after division.

# 13  //  3 = 4 (quotient of 13 divided by 3)
# Floor division operator (//) gives the quotient after division, discarding any fractional part.

# So the expression becomes:
# 3 - 4 = -1 